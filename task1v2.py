# Рисуем рёбра
nx.draw_networkx_edges(ukraine_graph, pos, edge_color='gray', width=1)

# Рисуем узлы
nx.draw_networkx_nodes(ukraine_graph, pos, node_size=200, node_color='skyblue', edgecolors='black')

# Рисуем метки
nx.draw_networkx_labels(ukraine_graph, pos, font_size=10, font_weight='bold')

plt.title("Map of Ukraine with Main Highways")
plt.axis('off')  # Отключаем оси координат
plt.show()


